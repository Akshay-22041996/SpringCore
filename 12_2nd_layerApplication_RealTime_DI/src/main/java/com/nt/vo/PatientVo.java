package com.nt.vo;

public class PatientVo {
	
	private String pname;
	private String paddress;
	private String perDayCharge;
	private String noOfDayAdmit;
	
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPaddress() {
		return paddress;
	}
	public void setPaddress(String paddress) {
		this.paddress = paddress;
	}
	public String getPerDayCharge() {
		return perDayCharge;
	}
	public void setPerDayCharge(String perDayCharge) {
		this.perDayCharge = perDayCharge;
	}
	public String getNoOfDayAdmit() {
		return noOfDayAdmit;
	}
	public void setNoOfDayAdmit(String noOfDayAdmit) {
		this.noOfDayAdmit = noOfDayAdmit;
	}
	
}
