package com.nt.vo;

public class GuestVO {

	
	private String guestName;
	private String guestAddress;
	private String perDayCharge;
	private String noOfDayStays;
	private String totalBillAmt;
	public String getGuestName() {
		return guestName;
	}
	public void setGuestName(String guestName) {
		this.guestName = guestName;
	}
	public String getGuestAddress() {
		return guestAddress;
	}
	public void setGuestAddress(String guestAddress) {
		this.guestAddress = guestAddress;
	}
	public String getPerDayCharge() {
		return perDayCharge;
	}
	public void setPerDayCharge(String perDayCharge) {
		this.perDayCharge = perDayCharge;
	}
	public String getNoOfDayStays() {
		return noOfDayStays;
	}
	public void setNoOfDayStays(String noOfDayStays) {
		this.noOfDayStays = noOfDayStays;
	}
	public String getTotalBillAmt() {
		return totalBillAmt;
	}
	public void setTotalBillAmt(String totalBillAmt) {
		this.totalBillAmt = totalBillAmt;
	}
	
	
}
