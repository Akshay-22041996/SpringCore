package com.nt.vo;

public class PatientVO {
	
	private String penroll;
	private String pname;
	private String padd;
	private String pdayCharge;
	private String noOfDayAdmit;
	public String getPenroll() {
		return penroll;
	}
	public void setPenroll(String penroll) {
		this.penroll = penroll;
	}
	public String getPname() {
		return pname;
	}
	public void setPname(String pname) {
		this.pname = pname;
	}
	public String getPadd() {
		return padd;
	}
	public void setPadd(String padd) {
		this.padd = padd;
	}
	public String getPdayCharge() {
		return pdayCharge;
	}
	public void setPdayCharge(String pdayCharge) {
		this.pdayCharge = pdayCharge;
	}
	public String getNoOfDayAdmit() {
		return noOfDayAdmit;
	}
	public void setNoOfDayAdmit(String noOfDayAdmit) {
		this.noOfDayAdmit = noOfDayAdmit;
	}

	
}
