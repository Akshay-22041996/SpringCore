package com.nt.vo;

public class CustomerVo {

	private String cid;
	private String cname;
	private String caddre;
	private String pamt;
	private String timePeriod;
	private String roInterest;
	private String intAmt;
	
	
	public String getIntAmt() {
		return intAmt;
	}
	public void setIntAmt(String intAmt) {
		this.intAmt = intAmt;
	}
	public String getCid() {
		return cid;
	}
	public void setCid(String cid) {
		this.cid = cid;
	}
	public String getCname() {
		return cname;
	}
	public void setCname(String cname) {
		this.cname = cname;
	}
	public String getCaddre() {
		return caddre;
	}
	public void setCaddre(String caddre) {
		this.caddre = caddre;
	}
	public String getPamt() {
		return pamt;
	}
	public void setPamt(String pamt) {
		this.pamt = pamt;
	}
	public String getTimePeriod() {
		return timePeriod;
	}
	public void setTimePeriod(String timePeriod) {
		this.timePeriod = timePeriod;
	}
	public String getRoInterest() {
		return roInterest;
	}
	public void setRoInterest(String roInterest) {
		this.roInterest = roInterest;
	}
	
	
	
}
